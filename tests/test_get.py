from bot_tests import BotTests


class HomePageTests(BotTests):

    def test_home_page_loads(self):
        self.assertEquals(self.app.get('/').status_int, 200)

    def test_home_has_useful_content(self):
        resp = self.app.get('/')
        self.assertEquals(resp.content_type, 'text/html')
        self.assertTrue(resp.content_length > 0)
        self.assertTrue("CorrelationBot" in resp)
